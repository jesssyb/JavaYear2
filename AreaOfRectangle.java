public class AreaOfRectangle
{
   public static void main(String [] args)
   {
      int widthOfRectangle = 5;
      int lengthOfRectangle = 8;
      int answer = widthOfRectangle * lengthOfRectangle;
      
      System.out.println("The area of the rectangle is: " + answer + " square units");
      
   }
}